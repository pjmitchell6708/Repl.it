package Replit;

public class Alphabet {
	public static void main(String[] args) {
		
		char letter = 'a';
		char bigLetters = 'A';
		while (letter <= 'z') {
			System.out.println(letter);
			letter ++;
			
		}
		
		while (bigLetters <= 'Z') {
			System.out.println(bigLetters);
			bigLetters ++;
		}
		
		
		
		
		
		
	}

}
